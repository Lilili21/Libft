#include "libft.h"

void	ft_putchar(char c)
{
	unsigned char ch[2];

	if (c >= 0)
		write(1, &c, 1);
	else
	{
		ch[0] = (3 << 6) + ((unsigned char)c >> 6);
		ch[1] = (1 << 7) + ((unsigned char)c & 63);
		write(1, ch, 2);
	}
}
